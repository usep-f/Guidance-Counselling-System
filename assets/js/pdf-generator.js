/**
 * PDF Generation Utility for Student Documents
 * Uses jsPDF (UMD version)
 */

export async function generateDocumentPDF(type, studentData, counselorSignature = "Guidance Counselor") {
  const { jsPDF } = window.jspdf;
  const doc = new jsPDF();

  // Branding Colors
  const primaryColor = [31, 185, 129]; // #1fb981
  const textColor = [15, 47, 42]; // #0f2f2a

  // Utility to add header
  const addHeader = (title) => {
    doc.setFont("helvetica", "bold");
    doc.setFontSize(22);
    doc.setTextColor(...primaryColor);
    doc.text("School Guidance System", 105, 30, { align: "center" });

    doc.setFontSize(10);
    doc.setTextColor(100, 100, 100);
    doc.text("Campus Guidance Office · Student Mental Wellness Support", 105, 38, { align: "center" });

    doc.setDrawColor(...primaryColor);
    doc.setLineWidth(0.5);
    doc.line(20, 45, 190, 45);

    doc.setFontSize(18);
    doc.setTextColor(...textColor);
    doc.text(title, 105, 60, { align: "center" });
  };

  if (type === "Good Moral Certificate") {
    addHeader("CERTIFICATE OF GOOD MORAL CHARACTER");

    doc.setFont("helvetica", "normal");
    doc.setFontSize(12);
    doc.setTextColor(...textColor);

    const bodyText = `This is to certify that ${studentData.name}, a student with Student No. ${studentData.studentNo}, enrolled in the ${studentData.program} program, has maintained a good moral character during their stay in this institution.\n\nThis certification is issued upon the request of the student for whatever legal purpose it may serve.\n\nIssued this ${new Date().toLocaleDateString('en-US', { month: 'long', day: 'numeric', year: 'numeric' })}.`;

    const splitText = doc.splitTextToSize(bodyText, 170);
    doc.text(splitText, 20, 80);

    // Signature Area
    doc.setFont("helvetica", "bold");
    doc.text(counselorSignature, 140, 160);
    doc.setFont("helvetica", "normal");
    doc.text("Guidance Counselor", 140, 165);
    doc.line(140, 158, 190, 158);

  } else if (type.startsWith("Admission Slip")) {
    addHeader("ADMISSION SLIP");

    doc.setFont("helvetica", "normal");
    doc.setFontSize(12);
    doc.setTextColor(...textColor);

    const isLate = type.includes("Late");
    const subtype = isLate ? "LATE ADMISSION" : "ABSENCE ADMISSION";

    doc.setFont("helvetica", "bold");
    doc.text(subtype, 105, 70, { align: "center" });
    doc.setFont("helvetica", "normal");

    doc.text(`Student Name: ${studentData.name}`, 20, 90);
    doc.text(`Student No: ${studentData.studentNo}`, 20, 100);
    doc.text(`Program: ${studentData.program}`, 20, 110);
    doc.text(`Reason: ${studentData.reason}`, 20, 120);

    const bodyText = `The above student is hereby cleared for admission to their classes.`;
    doc.text(bodyText, 20, 140);

    // Signature Area
    doc.setFont("helvetica", "bold");
    doc.text(counselorSignature, 140, 170);
    doc.setFont("helvetica", "normal");
    doc.text("Guidance Counselor", 140, 175);
    doc.line(140, 168, 190, 168);
  }

  // Footer
  doc.setFontSize(8);
  doc.setTextColor(150, 150, 150);
  doc.text("Generated by Guidance Counselling System", 105, 280, { align: "center" });

  doc.save(`${type.replace(/\s+/g, '_')}_${studentData.studentNo}.pdf`);
}
